package blog.xzbk.entity;

public class About {
	private int id;
	private int uid;
	private int type;
	private String content;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	@Override
	public String toString() {
		return "About [id=" + id + ", type=" + type + ", uid=" + uid + ", content=" + content + "]";
	}
}
